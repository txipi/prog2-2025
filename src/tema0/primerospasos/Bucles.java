package tema0.primerospasos;

public class Bucles {

	public static void main(String[] args) {
		// Todos los números naturales desde el 0 hasta el 49
		for (int i = 0; i < 50; i++) {
			System.out.println(i);
		}
		// Cuenta regresiva desde 10 hasta 0
		for (int i = 10; i >= 0; i--) {
			System.out.println(i);
		}
		// Todos los números naturales desde 50 hasta 500 de 10 en 10
		for (int i = 50; i <= 500; i += 10) {
			System.out.println(i);
		}
		
	}

}
